#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	int		size_d;
	size_t	i;

	i = 0;
	size_d = 0;
	while (src[size_d])
		size_d++;
	while (i + 1 < dstsize && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize != 0)
		dst[i] = 0;
	return (size_d);
}
